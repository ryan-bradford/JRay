package io.thaumavor.rbradford.JRay.Geometry;

public class Point3D {

	public double xPos;
	public double yPos;
	public double zPos;
	
	public Point3D(double xPos, double yPos, double zPos) { //An object to store X, Y, and Z
		this.xPos = xPos;
		this.yPos = yPos;
		this.zPos = zPos;
	}
	
}
