package display;

public class ColorPoint {

	public int xPos;
	public int yPos;
	public int redValue;
	public int greenValue;
	public int blueValue;
	
	public ColorPoint(int xPos, int yPos, int redValue, int greenValue, int blueValue) {
		this.xPos = xPos;
		this.yPos = yPos;
		this.redValue = redValue;
		this.greenValue = greenValue;
		this.blueValue = blueValue;
	}
	
}
